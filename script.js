    const lines = RAW.split('\n');
    let maxW = 0;
    lines.forEach(l => { if (l.length > maxW) maxW = l.length; });
    const W = Math.ceil(maxW * CW);
    const H = Math.ceil(lines.length * LH);
    canvas.width  = W;
    canvas.height = H;

    const particles = [];
    lines.forEach((line, row) => {
        for (let col = 0; col < line.length; col++) {
            const ch = line[col];
            if (ch === ' ') continue;
            const ox = col * CW, oy = row * LH + FS;
            particles.push({ ch, ox, oy, x: ox, y: oy, vx: 0, vy: 0 });
        }
    });

